package wrapperClasses;

public class WrapperDemo {

	public static void main(String[] args) {
		
		int i = 10;
		Integer x=new Integer(i);		
		System.out.println(x);
		
		float f=10.2f;
		Float y=new Float(f);
		System.out.println(y);
		
		
		byte b=40;
		Byte z=new Byte(b);
		System.out.println(z);
		System.out.println(b);
	}

}
